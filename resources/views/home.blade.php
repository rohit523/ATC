                <p>For some people, flying is an opportunity to see the world and to do so in style, soaring above the Earth, but for others the opportunity to sit for hours in close proximity to hundreds of other people is not one that is relished. Whatever your outlook on this form of transportation, it truly is a modern travel phenomenon. On some routes the mere existence of aviation has nearly eliminated all alternative modes of travel, especially ocean liners and thus some destinations are not practicably reachable any other way.</p>

                <h2><strong>Choosing a flight</strong> <i class="fas fa-plane"></i> </h2>

                <p>If you consider to fly, you might have the choice between several routes, flights, and airlines.</p>

                <h3><strong>Airports</strong></h3>

                <p>Take a look at all airports within reasonable distance overland. In cities like London, larger airports like Heathrow and Gatwick offer more full service (international) carriers, with lounges and airbridges, whilst the newer Luton and Stansted airports serve short haul budget carriers, with fewer shops and paid lounges, and are further from the city center. Some ticketing systems allow you to search using a code that covers more than one airport.
                </p>
                <p>Rail and bus transfer can be practical even for long distances; for instance Tokyo Narita Airport has rail connections to most of Japan. A well-connected airport can be the best option even for neighboring countries. Vienna International Airport does not only serve eastern Austria, but also nearby provinces in the Czech Republic, Slovakia, and Hungary.
                </p>

                <h3><strong>Destinations</strong></h3>

                <p>
                    It generally pays to double check that the ticket you bought is for a flight going to your actual intended destination, as multiple cities and towns can often share the same name. For instance, San Jose can refer to a city in the United States, a city in Mexico, or the capital city of Costa Rica. There have been even cases of passengers heading for Sydney, Australia mistakenly purchasing a ticket for Sydney, Canada, which is located 17,056 km (10,598 miles) away. Check the Airport code of your destination as well as departure point and transfers.
                </p>

                <h3><strong>Time of departure and arrival</strong></h3>

                <p>
                    If your flight date is flexible, and neither departure nor destination is in the middle of nowhere, you can often choose between daily flights. A busy route has many flights a day.
                </p>
                <p>
                    When booking flights, keep in mind that as far as flight scheduling is concerned, a new day starts at 12 midnight. For instance, if your flight is scheduled to depart at 00:10 on 1st April, you will need to be at the airport by 22:10 31st March to check in. Many travellers have missed flights due to this confusion by turning up to check in at 22:10 on 1st April instead.
                </p>
                <p>
                    While a business or weekend traveller might have few choices, a patient traveller can usually find a cheaper flight. As a rule of thumb, Monday mornings, Friday evenings and major holidays and events tend to be overbooked; these times are not only more expensive, but also less pleasant both on board, at the airport, and in airport transfer.
                </p>
                <p>
                    Time of departure and arrival affect the availability of airport transfer and amenities. If you depart or arrive in the middle of the night, you might find yourself stranded at either airport for hours, with few stores and restaurants open, if any.
                </p>
                <p>
                    If you sleep well on aircraft, or you can afford business class, an overnight flight saves time, as well as the cost of a hotel night. Beware of time zones and jet lag, though.
                </p>

                <h3><strong>Airlines</strong></h3>

                <p>
                    While airlines differ in price, quality and reputation, they can be roughly divided into these categories:
                </p>

                <ul>
                    <li>
                        A <strong> legacy </strong> carrier, also called a major airline or full-service carrier, is an airline where cost and service level is above average even for economy class (though bargains might be found). Passengers can pay extra for business class seats, and some aircraft have a first class. Many of these airlines have a heritage from the days when flying was a luxury.
                    </li>
                    <li>
                        A <strong> budget airline </strong> , low-cost carrier, startup airline or no-frills carrier offers a minimum of land and in-flight service. Extra fees for booking and check-in service (except when booked online and paid upfront), insurances, baggage, seat selection, meals and even water might in total cost more than the ticket itself. These carriers are usually not part of an airline alliance, and often call at less busy airports, far from the nominal destination. Usually, budget airlines only sell point-to-point tickets, with no responsibility assumed for any missed connections. If you are scheduled to arrive late at night, air transit might be closed for the day, without compensation. When the flight is full of passengers there is possibility that some of the baggage will not fly with the passengers and will arrive later on another flight.
                    </li>
                    <li>
                        A <strong> regional airline </strong>  operates on less-travelled routes, usually with smaller aircraft. Some of these are owned by, or associated with, a legacy carrier. Expect prices on such routes to be higher than those between major hubs for the same distance travelled.
                    </li>
                    <li>
                        A <strong> charter airline </strong> lets out planes to a client, such as a travel agent. While they dominate the resort flight market, flights are advertised through the travel agency, so the airline brands are not well known among the public.
                    </li>
                    <li>   
                        General aviation includes all kinds of unscheduled civil aviation, such as business jets, and bush planes. They are useful for a VIP in a hurry, or for reaching isolated places.
                    </li>
                </ul>

