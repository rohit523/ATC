                <h3><strong>Flying with Children</strong></h3>

                <p>Children can get restless and irritable while flying and in airports. There are strategies you can
                    follow to ensure your children enjoy the trip.
                </p>
                <ul>
                    <li> Arrange entertainment. The best way is to bring a portable media player, books, or anything
                        else
                        they can use to stay occupied with themselves. Be creative. Phones, iPods and PSPs also play
                        video
                        these days, and are much easier to carry than a DVD player and DVDs. Kids don't seem to mind the
                        fact that the screen is 1" square, and the batteries last far longer than a DVD player for
                        longer
                        flights.
                    </li>
                    <li> Have something for them to suck on while ascending and descending. Don't give it to the child
                        when
                        you get onto the plane - wait until you are taxiing to the runway, or it will be finished before
                        you
                        take off. Similarly, wait until well into the descent.
                    </li>
                    <li> Bring favorite snacks for fussy eaters. If children don't like the airplane food and get
                        hungry,
                        irritability can increase. If flying to another country, ensure snacks not consumed won't
                        violate
                        its regulations.
                    </li>
                    <li> Aim for a window seat for the child, and sit by the window at the airport. Airports are a hive
                        of
                        activity, usually enough to keep any child occupied for a little while.
                    </li>
                    <li> Get an airport book. There are many picture books for young children that name the many things
                        at
                        the airport. For older children at a large airport, an airplane identification chart can pass
                        some
                        time.
                    </li>
                </ul>
                <p> Consider safety. If you are traveling with a child who is less than three, have them sit on an
                    approved child carrier, not on your lap. (Approved or airline carriers may have special seat belts
                    that improve their protection.) In the unlikely event of an emergency, a lap child may impede your
                    ability to brace. Be aware of whether there is an oxygen mask for infants on the aircraft/row.
                </p>

                <p> And generally, anticipate delays. Even the shortest flights can be delayed, involving additional
                    time both in the terminal and on the aircraft. Ensure you have sufficient food, clothes, nappies,
                    and entertainment to avoid turning a couple of hours delay into a nightmare. This will free them to
                    do truly essential flight tasks.
                </p>

                <p> Some airlines, rather controversially, do not permit male passengers to be seated next to
                    unaccompanied children. If you are seated next to an unaccompanied child, you will be made to swap
                    seats with a female passenger.
                </p>

                <h3><strong>Before take off</strong></h3>

                <ul>
                    <li>
                            Buckle up and strap any children, in safety seats as needed, then yourself.
                    </li>
                    <li> Count the number of seat backs between your seat and the emergency exits, keeping in mind
                        that your
                        nearest exit may be behind you. If you ever need to evacuate an aircraft in an emergency you
                        may
                        need to do it in a darkened cabin that could be full of thick black smoke. If the aisle is
                        full of
                        people you will at least know the number of seats you need to climb over to get out in that
                        one in a
                        million emergency.
                    </li>
                    <li> Put your mobile phone and any other transmitting device into flight mode or switch it off,
                        preferably before you board the plane. Using a phone on board after the doors are closed
                        (especially
                        while the aircraft is taxiing, climbing or descending) is a violation of some air travel
                        safety
                        regulations; in some countries, switching the phone off is mandatory during the passenger's
                        entire
                        stay in the aircraft. Switching the phones off is intended to assure clear, effective and
                        essential
                        communication between pilots and air traffic controllers. If you refuse to comply, you will
                        be
                        escorted off the plane.
                    </li>
                    <li> If you wish to use Bluetooth headphones or the in-flight Wi-Fi service, you still have to
                        place your
                        device in airplane mode before takeoff to block the mobile phone signals. Once you are
                        allowed to
                        use your device in the air, you can individually turn on the Bluetooth and Wi-Fi functions.
                        For
                        tablets and laptops with no mobile phone service, you can turn off the airplane mode
                        altogether.
                    </li>
                    <li> If you need to make a call while at altitude, your aircraft's telephone carrier partner may
                        provide
                        in-flight service. Consult your in-flight magazine, especially for details on
                        charges...normally
                        about $5/minute (plus connection charges) even if you are directly above the place you are
                        calling.
                        SMS on these in-seat handsets may also be available.
                    </li>
                    <li> Read the emergency instructions and watch the safety briefing even if you have ridden on
                        the airline
                        Before as safety features may vary per aircraft and airline. It may be boring but if an
                        emergency
                        happens you will remember what to do, rather than having to read the safety card then and
                        thereby
                        saving precious time.
                    </li>
                    <li> Place anything containing items you'll use in-flight under the seat in front of you to
                        eliminate
                        obstructing the aisle...if they are small, in the seat pocket facing you. This will minimize
                        disturbance caused to those sitting in aisle seats. If you later need the leg/foot room, and
                        overhead space is available, you can then move to there what you no longer need.
                    </li>
                    <li> Keep within sight anything you put in overhead bins that contains valuables. As above,
                        thieves
                        operate on flights. As necessary, put valuables under the seat at your feet.
                    </li>
                    <li> Once seated, and if you have them, use sanitizer/sanitizing wipes to clean your hands, tray
                        table,
                        arm rests and (when convenient) the handles on overhead bins.
                    </li>
                </ul>

                <h2><strong>During Flight</strong></h2>

                <ul>
                    <li> In cases of an open flight in economy class when nobody is beside you, feel free to put up
                        the
                        armrests (except in exit rows where the armrests can't be lifted) to claim the extra space
                        to
                        yourself. On wide-body aircraft, you can sometimes get a middle block to yourself and turn
                        this into
                        a flat bed of sorts.
                    </li>
                    <li> Wear your seat belt at all times while seated. Though it doesn't happen often, more people
                        are
                        injured (a few even killed) by failing to use "belts" than from all other causes of flying
                        injuries.
                        Severe air turbulence can occur without warning even in clear air, and it can violently
                        throw you
                        and others about. When the seat belt sign is off, it only indicates that you'll probably be
                        reasonably safe to move about the cabin briefly.
                    </li>
                    <li> When getting up from other than the aisle seat, first ask seatmates to let you out, and try
                        not to
                        disturb people behind or in front of you.
                    </li>
                    <li> As a good neighbor, check in advance with the folks behind you if you may/must tilt your
                        seat-back
                        backwards...and just before you do so. The pitch of many economy seats has gotten so small
                        that
                        tilting seat-backs can greatly intrude into space they really need.
                    </li>
                    <li> On medium-to-long flights, drink lots of liquids without caffeine or alcohol because the
                        latter will
                        dehydrate you at an even faster pace than the airplane's very low humidity. That can worsen
                        jet lag
                        and may induce headaches. Don't hesitate to ask the cabin crew for water, or walk up to the
                        galley
                        to get it. Some airlines (e.g., Emirates, Qantas, Cathay Pacific) offer self-service water
                        fountains
                        (and more) at each galley for passengers to fill their own water bottles. If sanitation is
                        an
                        unknown, ask for bottled water.
                    </li>
                    <li> Don't sit still for the duration of a multi-hour flight...your body isn't designed to stay
                        that way
                        for hours.
                    </li>
                    <li> Adjust your body position occasionally (you do this in your sleep anyway). On long flights
                        especially...
                    </li>
                    <li> Stretch, flex knees, move your feet in circles...anything you can do in your seat. Some
                        airlines now
                        periodically show video programs showing how to exercise in your seat. Follow them, or do
                        your own
                        thing (or both). By changing position, and moving around a little, you make sure every part
                        of your
                        body gets the circulation it needs, e.g., to avoid deep vein thrombosis (DVT).
                    </li>
                    <li> Consider walking around the aircraft occasionally during long flights. Feeling
                        self-conscious about
                        just walking around? Stroll off to the lavatory or galley, or stand up and pretend that you
                        need
                        something out of the overhead bin once an hour. If you have an aisle seat, stand up every
                        time your
                        neighbors want to move, instead of hoping that they'll squeeze past you.
                    </li>
                    <li> You might remove your shoes if convenient. On very long flights, better airlines offer
                        slippers upon
                        request, but you'll likely also need warm socks. Always wear shoes in the lavatory, where
                        the floor
                        may be wet.
                    </li>
                    <li> With the exception of some private charters, smoking (even electronic cigarettes) is not
                        allowed on
                        any commercial flight worldwide. Do not smoke at your seat, in the lavatories or tamper with
                        the
                        smoke detectors in the lavatories to avoid being caught â€“ at minimum you'll be in trouble
                        with the
                        airline and in some jurisdictions subject to prosecution. In U.S. "flagged" aircraft (and
                        many
                        others), federal law prohibits tampering with, disabling, or destroying smoke detectors in
                        aircraft
                        lavatories...it's a felony and violation can lead to fines of thousands of dollars, even a
                        prison
                        sentence. Worried about how you'll survive? Try nicotine patches, nicotine gum, or other
                        tobacco
                        cessation tools. Plan for ways to handle anxiety, if that's one of your triggers. Also, plan
                        other
                        things to do. The immediate urge to smoke, even when it's strong, will subside while you're
                        busy
                        watching funny cat videos or listening to your favorite music. You may also take advantage
                        of the
                        long flight as an opportunity to quit smoking. If you resist lighting up and using other
                        forms of
                        nicotine for 72 hours since your last cigarette, the physical urges will have passed, and
                        you'll be
                        well on your way to staying tobacco-free.
                    </li>
                    <li> Stow any loose items before landing. Put magazines and books in the seat pocket. Don't
                        leave
                        anything lying on an empty seat or loose under your feet. Under heavy braking on landing,
                        even light
                        articles can slide/roll quite far forward. They can be difficult to find, and before you
                        find them
                        they may be accidentally damaged by exiting passengers.
                    </li>
                    <li> Always follow the instructions of flight attendants, as well as lighted and posted signs.
                        They are
                        there for your safety and for that of all passengers.
                    </li>
                    <li> Flight attendants are trained to be responsible for your safety. Do not argue with them.
                        Resolve any
                        conflict you may have with their orders later on the ground.
                    </li>
                    <li> If an "unruly passenger" becomes a serious problem, the captain of the plane may divert to
                        a nearby
                        airport where the passenger will be arrested by local police and escorted off the airplane.
                    </li>
                    <li> Flight attendants are often backed by law, e.g., in the US, interfering or physically
                        assaulting a
                        flight attendant is a felony. Short of that, you still can face severe fines and costs for
                        disobeying.
                    </li>
                    <li> Most airlines (including all US flagged carriers, due to local civil aviation law) prohibit
                        the use
                        of mobile phones in flight, unless placed into "flight safe mode" or "aircraft mode" before
                        departure. A few carriers (e.g. Emirates and Virgin Atlantic on some aircraft types) are now
                        permitting the use of mobile data and even voice calls while airborne. Keep in mind that
                        you'll need
                        an international roaming contract with your carrier and rates tend to be very high (US$3-$5
                        per
                        minute or $1 per megabyte is fairly standard.) Beware, a gigabyte is around $1,000! If voice
                        calls
                        are permitted, be courteous to your fellow passengers and keep conversations brief.
                    </li>
                    <li> Other electronic devices are usually permitted once the aircraft is at cruise and the seat
                        belt sign
                        is switched off. Rules regarding when and which devices can be used vary by country and
                        airline:
                        In Australia and the United States, airlines typically list categories of devices that
                        cannot be
                        used on the safety card (cell phones, TV's, remote controlled toys, etc...) in their
                        magazines.
                    </li>
                    <li> In Canada, however, aviation law requires a more precautionary approach. No electronic
                        devices are
                        permitted unless a crew member individually checks and authorizes their use.
                    </li>
                    <li> Laptops are the only devices that are explicitly stated in safety briefings as being
                        allowed â€“ as
                        long as you check with a flight attendant and any mobile data capabilities are disabled
                        before use.
                    </li>
                    <li> In the past, Air Canada used to state that the use of external laptop accessories (such as
                        hard
                        drives, mice, printers, etc...) was prohibited. The current safety video doesn't mention
                        this, but
                        always check first.
                    </li>
                    <li> Other devices such as tablets, iPods, and game systems are at the crew's discretion but are
                        almost
                        always permitted if you ask.
                    </li>
                </ul>
