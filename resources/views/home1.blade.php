                <p>You've now arrived at your local airport, ready to 'slip the surly bonds of Earth' and walk
                    several miles to your gate. But while it may feel that airports force you to walk the vast
                    majority of the way towards your destination, this article should help expedite and ameliorate
                    your passage through any airport: from check-in to duty-free checkout.

                    This part of the guide offers extended tips for that part of your journey by air between the
                    entrance door of the airport terminal until the time you board your aircraft.

                    Most international airport terminals are standardized, with similar procedures for check-in,
                    security, immigration and customs, however, it's still worth considering the particular quirks
                    of your point of departure. Wikivoyage has articles about many individual major airports
                    worldwide, whilst an internet search should be able to provide any additional information that
                    you may need for your trip.

            <h2><strong>Boarding Pass</strong></h2>

            <p>The flight ticket does not enable you to board the plane: you must have a boarding pass. Check-in is
                the process
                of producing your boarding pass, which includes seat numbers, departure times and gates. As a
                general rule, you
                should aim to be at the airport at least 2 hours for domestic flights (3 hours for International
                flights) before
                the scheduled departure time of your flight. In the security check, only passengers with boarding
                passes are admitted
                in. You can often do the check-in yourself electronically, either online or with check-in kiosks at
                the airport, some
                airlines require you to check in only prior to their check in counters at the airport opening to
                avoid sometimes outrageous
                fees. Check-in is not to be confused with baggage drop, which requires prior check-in.</p>

            <p> With online check-in becoming more and more the standard, some airlines no longer strictly
                distinguish between baggage drop
                and check in. If the queues are short, the agent at the bag drop counter might check you in, assign
                a seat and take your bag.
                Don't count on it though. In addition, for international flights, particularly to the United States,
                you may still be required
                to present yourself to counter staff even if you have checked in online so your passport and visas
                can be verified.</p>

            <p>
            With some airlines you will receive a boarding pass with a seat assignment, while some do not assign
            seats. You will need a boarding
            pass to present to the security staff and later to the gate staff when boarding the flight. At this
            time, your checked luggage will
            be weighed, labelled, and handed off to baggage handlers.</p>

            <h3><strong>Landside and airside</strong></h3>

            <p>Airports consist of two areas: landside with the check-in and baggage drop, and airside with the
                aircraft. To get airside, passengers
                must clear the security barrier (see below). International airports have immigration and customs
                controls for passengers going from
                airside to landside (see arriving by plane for details). In other words, passage between these areas
                takes time, and should preferably
                be done just once. To use the airside to landside passage backwards without permission is strictly
                forbidden. People who forget something
                airside must sort it out with the security staff.</p>

            As the landside area is usually open to the public, it might be more crowded, dirty, and more exposed to
            pickpockets and other crime than
            airside. Unticketed persons are often not allowed to stay with family or friends as they go through
            processing to airside. Some countries may
            allow family and friends to accompany domestic passengers airside up to the boarding gate provided they
            go through security; check each country's
            regulations for more details.</p>

            <h3><strong>Boarding Times And Delays</strong></h3>

            <p>
                Your boarding ticket specifies Boarding time, which is when boarding starts (not when it ends).
                Usually the boarding starts even after the
                printed time, but for short flights at least 30 minutes before departure; for international flights
                on large aircraft, sometimes at least
                45 minutes. Some airlines do what is called "pre-boarding" with passengers admitted into a separated
                area after having their boarding passes
                checked in order to avoid a check when the airplane arrives. It is often possible to leave this
                "pre-boarding zone" only after showing the
                boarding pass again.

                <h3><strong>Delays and Cancellations </strong></h3>

                <p>When a flight is cancelled, the reason given is usually some kind of technical or weather-related
                    problem. Sometimes the real reason is that
                    so few passengers have checked in that it is cheaper for the airline to cancel the flight and
                    rebook the passengers on a later flight, or
                    even on another airline. If a flight is canceled, the airline is obligated to get you on the
                    next available flight to your destination, but
                    interpretations of "next available" vary and, for some low-cost carriers like Ryanair, this may
                    mean a long wait indeed. With flights operated
                    by European Union (EU) carriers to or from the EU, passengers are entitled to cash compensation
                    of €250 and more after a delay of 3 hours or
                    more - unless the delay is for circumstances beyond the carriers control.

                    Be aware that unusual weather can cause the very strange phenomenon of being denied boarding
                    because of weather for a flight that, nevertheless,
                    does depart on schedule. This is usually caused by weight limits and takes two forms:<br>
                    <p>
                        <ul>
                            <li> Predicted weather may make the flight longer, and so more reserve fuel is required.
                                Most planes
                                can't take a full load of passengers and full fuel;
                                if they must fill the tanks more than expected, they might have to leave some people
                                behind. This is more likely on long distances, but can happen
                                on short flights if the runway is short (heavy aircraft accelerates slower and needs
                                longer runway).</li>



                            <li>
                                As it gets warmer, the take-off roll increases (the air is less dense and so
                                decreases
                                wing lift as it slightly decreases engine thrust), but the
                                runway doesn't get any longer. If the air temperature gets hot enough (e.g., summer
                                in Las
                                Vegas or Denver), they may have to reduce weight for the
                                plane to get safely in the air.</li>
                        </ul>
                    </p>
                </p>

                <h3><strong>Overbooking</strong></h3>

                <p>
                    Overbooking is the practice of accepting reservations for a flight from more people than can fit
                    on the plane. Almost all airlines overbook their flights, as statistically some percentage of
                    passengers do not show up for the flight.</p>
                <p>If you want to avoid being bumped you should get seat allocation as soon as possible. Sometimes
                    a travel agent can do this when reserving your flight, sometimes you can do it electronically
                    with your reservation or by checking in early on the Internet before arriving at the airport.
                    If you have no seat allocated to you then you are at risk of being bumped at the airport, even
                    if you arrive a long time before other passengers who may already have seats allocated.</p>

                <h2><strong>Check-in</strong></h2>

                Check in consists of three main parts, which used to be performed at the same time but can now be
                done separately:
                <ol>
                    <li>The actual check-in, i.e. confirming to the airline that you will take the booked flight and
                        that the airline should expect your arrival... and possibly, look for you should you not
                        arrive at the gate on time (depending on specific airline policies)</li>
                    <li> Handing your hold luggage to ground handling staff (checking it in), who will tag it for
                        your ticketed destination, and take care of it on its way to your airplane's luggage hold
                    </li>
                    <li>Getting your boarding pass to enable you to continue further</li>
                </ol>
                <h4><strong>Traditional check-in</strong></h4>
                <p>
                    The first thing you will need to do at the airport is check in for your flight. This entails
                    proceeding
                    to a check-in desk allocated for your airline or flight. Present your passport or other photo ID
                    (national
                    ID card in Europe or driver's license in North America), and a confirmation of your ticket if
                    asked, to
                    airline staff at your flight's designated check-in counter or at the common check-in counter,
                    depending on
                    your airline. You will also need to present your visa to staff if this is required for your
                    destination.

                </p>
                <h4><strong>Automated check-in at the airport</strong></h4>
                <p>An increasing number of airlines are implementing a self-check-in system at certain airports. In
                    most cases
                    this option is available to passengers with or without check-in bags. Especially in Europe, this
                    is one of
                    the only ways (aside from online check-in) for passengers (especially those travelling on
                    economy) to check
                    in as staffed reception desks are increasingly relegated as 'baggage drop only' desks. These
                    systems involve
                    small kiosks in which you can enter your booking reference, swipe or insert the credit card used
                    to make the
                    booking or swipe/insert your frequent flyer card (if it has a magnetic strip) to access your
                    record and print
                    out a boarding pass for you and your travelling party. You may have the opportunity to change
                    your seats when
                    checking in; in particular, many airlines do not open the exit rows until the day of the flight.
                </p>
                <h3><strong>Security Check</strong></h3>
                <p> Aviation security is no laughing matter. Even before the airliner attacks on New York and
                    Washington in 2001, most
                    countries took it very seriously, but since then security regulations have been tightened, and
                    airport security
                    personnel will be very strict in implementing them. This check takes place between landside and
                    airside.</p>

                <p>In some countries, before boarding an international flight, you may not only be required to pass
                    a security check,
                    but also a customs and immigration screening.</p>
                <h4><strong>Landside Security</strong></h4>
                <p>Airports with an elevated need for security (such as Ben Gurion International Airport in Israel)
                    might have early
                    checkpoints; at the road to the airport, or at the building entrance. Officers might request
                    documents and/or a
                    scanning of travellers and their baggage.</p>

                <p> International airports usually have a high level of surveillance which is justified as being
                    there to prevent crime,
                    illegal immigration and smuggling, at the cost of visitors' privacy. In and around airports,
                    staff might use harsher
                    methods than in civilian locations, against unattended baggage, loitering and drunkenness. The
                    civil rights implications
                    of all this - and whether any given "security" measure actually works are sometimes subject of
                    public debate. Plenty of
                    time margins, papers in order and good behavior are the best methods to deal with excessive
                    security measures.</p>



                <h4><strong>The Process</strong></h4>
                <P>You'll usually check any luggage with the airline at the ticket counter. It will be at least
                    electronically screened for security as it goes to a holding area to be loaded on your flight.
                </P>

                <P>Then, as you walk to your gate, you and your carry-ons must go through personal security
                    screening. It involves the following basic steps (varying based on individual countries'
                    policies and on whether the flight is domestic or international).</P>
                <UL>
                    <LI>You must present identification (perhaps except for toddlers) and boarding passes for your
                        flight. Keep your ID and boarding passes with you throughout the process.</LI>
                    <LI>You may be instructed to:</LI>
                    <LI> Remove bulky outer garments (e.g., sweaters, jackets) and/or your shoes... and place them
                        in a bin/tub going through separate electronic scanning or manual inspection.</LI>
                    <LI> all carry-on bags, purses, laptops and the clear bag of liquids in tubs/bins on the line
                        for separate scanning. Laptops, other electronics, and liquids may have to be placed
                        separately. Put the clear bag of liquids on top where it can be easily seen and inspected.
                    </LI>
                    </LI>
                    </LI>>>
                    <LI>This is your last opportunity to avoid delays, by placing all metal or electronic items
                        (e.g., cell phones, coins, keys) into a bin/tub.</LI>
                    <LI>Proceed to a nearby point for body screening: electronic, X-ray, and/or manual. Any metal
                        object will generate an alarm. You'll be directed to return to the carry-on scanning line
                        and place "offending" items in a bin/tub for separate screening. If this happens, be careful
                        not to leave them behind when grabbing your bags!</LI>
                </UL>
                <h2><strong>Boarding</strong></h2>
                <p>
                    The order of boarding may be specified by the gate attendant, often:

                    <ol>
                        <li>Passengers in First class</li>
                        <li>Passengers with special needs (such as physically handicapped, elderly and those with
                            young children)</li>
                        <li>Passengers in Business Class and those passengers holding top tier cards of an airline
                            alliance's Frequent Flyer program</li>
                        <li>All other passengers. For large aircraft, you may be assigned a "group" seating
                            designation intended to organize overall boarding.</li>
                    </ol>
                    <p>Some budget airlines (that do not assign seating) board passengers who have paid extra for
                        priority boarding first.</p>

                    <p>For larger aircraft with allocated seating, boarding is requested by group or row number.
                        This may allow those furthest away from the boarding doors to board first so the aisles
                        don't get constricted. Enforcement of this is lax, and many times the aisles become
                        overcrowded. Regardless of the boarding order given, you are always free to remain in the
                        boarding lounge until the final call for the plane. If you choose to spend the least time
                        possible in a cramped aircraft cabin, just wait in the boarding lounge until you see the
                        last person at the gate, then promptly join the end of the queue. Remember:</p>
                    <ul>
                        <li>The boarding gates close 10–20 minutes before departure (sometimes more) and you'll
                            likely hear no announcements to that effect outside the gate area.</li>
                        <li>The last boarders (especially in economy class) may find all overhead luggage space
                            full. Any carry-ons that cannot fit underneath the seat in front of you may have to be
                            checked with any valuables they have inside</li>
                    </ul>
                </p>
                <h2><strong>The Gate</strong></h2>
                <p>There are three main kinds of boarding.
                    <ul>
                        <li> Tarmac walk. Most common at small airports and for low-cost carriers. Passengers walk
                            across the
                            tarmac to the plane.</li>
                        <li>Jet bridge. Most common at large airports. A jet bridge (which in best case is sealed
                            from
                            outdoor air) takes the passengers into the plane.</li>
                        <li> Bus transfer. Most common at peak hours at crowded airports. Passengers get on a bus,
                            which
                            takes them to the plane. In this case, passengers need to be at the gate in advance; as
                            long as
                            30 minutes before departure.</li>
                    </ul>
                </p>
